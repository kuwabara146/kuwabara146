
public class task2 {

	public static void main(String[] args) {
		int a = 10, b = 12, c = 13;
		int d = a + b + c;      //合計

		System.out.println("合計:"+ d);

		int e = d / 3;          //3で割った時の平均

		System.out.println("平均(3で割った時):" + e);

		double f = d / 3.0;    //3.0で割った時の平均

		System.out.println("平均(3.0で割った時):" + f);
	}

}
